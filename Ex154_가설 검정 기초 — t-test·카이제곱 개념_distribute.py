# 가설 검정 기초 — t-test·카이제곱 개념 

import numpy as np
from scipy import stats
# pip install scipy

# 검정 : 모집단에 대한 어떤 가설이 통계적으로 타당한지 표본 데이터를 사용해 판단하는 과정
# 이 예제는 각 반 1000명의 평균이 같다는 "가정"하에 시작
# 데이터 생성(1000명 중 8명을 뽑은 표본 데이터라고 생각하자.)
class_a = [85, 90, 78, 92, 88, 84, 89, 91]
class_b = [75, 82, 79, 85, 70, 78, 80, 81]

mean_a = np.mean(class_a)
mean_b = np.mean(class_b)

print(f"A반 평균: {mean_a}") # 87.125
print(f"B반 평균: {mean_b}") # 78.75 

print(f"A반 표본분산: {np.var(class_a, ddof=1)}") # 21.267857142857142 
print(f"B반 표본분산: {np.var(class_b, ddof=1)}") # 21.071428571428573 

print(f"A반 표본 표준편차: {np.std(class_a, ddof=1)}") # 4.611708700997618
print(f"B반 표본 표준편차: {np.std(class_b, ddof=1)}") # 4.590362575159894

# 표준편차 : (각데이터-평균 )을 제곱해서 n-1로 나눈값에 루트를 씌운것
# 표준편차 : 표본 데이터들이 평균값으로부터 얼마나 흩어져 있는지를 나타내는 지표

# 1. 표준오차 수동 계산 (표준편차 / sqrt(데이터개수))
# 아래는 표준 오차 구하는 방법
std_dev = np.std(class_a, ddof=1)
n = len(class_a)
se_manual_a = std_dev / np.sqrt(n) 

# 2. scipy.stats 사용 (권장) : 표준 오차(Standard Error of the Mean, SEM)
se_scipy_a = stats.sem(class_a)

print(f"직접 계산한 표준오차: {se_manual_a:.4f}") # 1.6305
print(f"함수로 계산한 표준오차a: {se_scipy_a:.4f}") # 1.6305
print(f"함수로 계산한 표준오차b: {stats.sem(class_b):.4f}") # 1.6229

# 표준오차: 1000명 중 8명씩 뽑아서 평균을 내는데 그 평균들의 표준편차가 표준오차다. 
# 표본 평균들이 전체 평균(모평균)으로부터 얼마나 떨어져서 분포하는가"를 나타내는 지표

# t-test 수행
t_stat, p_value = stats.ttest_ind(class_a, class_b) 

# stats.ttest_ind() :
# t_stat (T-통계량): 
# 의미: "두 집단의 평균이 얼마나 떨어져 있는가?"를 나타낸다.
# T-통계량은 단순히 평균의 차이만 보는 것이 아니라, "평균의 차이가 데이터의 흩어짐(분산)에 비해 얼마나 큰가?"를 본다. 
# 값이 커질수록(절댓값) 두 집단의 평균 차이가 크다는 것을 의미한다.

# SE_a^2 = 1.6305^2 = 2.6585
# SE_b^2 = 1.6229^2 = 2.6338
# sqrt(2.6585 + 2.6338) = sqrt(5.2923) = 2.3005 => 두 평균의 차이(평균_A - 평균_B)의 표준오차(Standard Error of the Difference)

# 최종 T-값 계산: 8.375/2.3005=3.6405
# 우리가 구한 평균 차이(8.375)는 합쳐진 표준오차(약 2.3)보다 약 3.64배나 크다
# 결론: 통계학에서는 "평균의 차이가 오차 범위를 3.64배나 초과했다"고 봅니다. 

# 표준오차가 '매우 클 때' : 불안정한 데이터
# 표준오차가 '매우 작을 때' : 안정적인 데이터

# t_stat값으로 p_value를 구한다.

# p_value (유의 확률): 
# "두 집단이 사실은 똑같은데, 우연히 이만큼 차이가 날 확률"을 의미합니다.

print(f"T-statistic: {t_stat:.4f}")
print(f"P-value: {p_value:.4f}")

# 결과 해석
if p_value < 0.05: 
    print("결과: 두 반의 평균 점수는 통계적으로 유의미한 차이가 있습니다.")
    
else:
    print("결과: 두 반의 평균 점수에 유의미한 차이가 없습니다.")


print('----------------------------------------')


# 2. 카이제곱 검정 예제: 성별과 선호 제품(A vs B)의 관계
# 성별에 따라 선호하는 제품에 차이가 있는지(독립적인지) 확인합니다. 교차표(Contingency Table)를 사용합니다.
# 특정 성별이라고 해서 화장품을 더 많이 혹은 더 적게 구매하는 경향이 나타나지 않는것 이것이 독립상태다.

import numpy as np
from scipy import stats


# 관측 빈도 (행: 성별, 열: 제품)
# 남성: 제품A 20명, 제품B 30명 / 여성: 제품A 35명, 제품B 15명
data = np.array([[20, 30], 
                [35, 15]])

#      제품A 제품B	합계
# 남성	20	 30	    50
# 여성	35	 15	    50
# 합계	55	 45	   100

print('# 카이제곱 검정 수행')
chi2, p_value, dof, expected = stats.chi2_contingency(data)

# chi2 : 
# 실제 데이터와 기대 데이터가 얼마나 다른지를 나타내는 점수(score)가 chi2. 실제 데이터와 독립적인 기대 데이터 간의 차이
# 점수가 클수록: 실제 데이터가 기대 데이터(독립 가정)에서 멀리 떨어져 있다는 뜻. 즉, "두 변수는 독립이라고 보기 어렵다(종속 관계일 가능성이 높다)"는 증거가 강해진다.

# p_value (유의 확률): 
# 카이제곱 통계량이 우연히 발생했을 확률
# 일반적으로 이 값이 0.05 미만이면, 두 변수가 독립(서로 관계없다)이라는 가설을 기각하고 "두 변수는 서로 관계가 있다"고 통계적으로 결론을 내린다.

# dof (자유도): 
# 분석에 사용된 표의 크기에 따라 결정되는 통계적 파라미터
# (행의 개수 - 1) × (열의 개수 - 1)로 계산되며, 어떤 분포를 기준으로 chi2가 큰지 작은지를 판단할 때 기준이 된다.

# expected (기대 빈도): 
# 두 변수가 완벽하게 독립 상태라면 각 칸에 있어야 할 이론적인 수치
# 실제 데이터(observed)와 이 기대 빈도를 비교하여 차이를 계산하게 된다.

# 전체 합계: 남성 50명, 여성 50명 / 제품A 55명, 제품B 45명 / 전체 총합(N) = 100명
# 계산 공식: (행 합계 × 열 합계) / 전체 총합
# 예: 남성-제품A의 기대 빈도 = (50 X 55) / 100 = 27.5 이 공식을 모든 칸에 적용하면 expected 행렬이 나옵니다. 
# 이것이 독립상태에서 기대값이다. 독립이라면 성별은 제품 선택에 영향을 주지 않는다.
# 27.5는 "성별이 아무런 간섭(영향)을 하지 않았을 때, 수학적 확률론에 따라 당연히 나타나야 하는 공평한 숫자"이기 때문에 '독립 상태의 기대값'이라고 부른다

# chi2:
# 실제 관측값(data)과 방금 구한 기대값(expected)이 얼마나 차이 나는지 측정합니다.
# ((관측값(O)-기대값(E))**2) / 기대값 을 모두 더한것
# 첫번째 셀 : (20-27.5)**2 / 27.5 = 2.04545
# 4개의 셀 모두 더하면 χ2=2.04545+2.5+2.04545+2.5=9.0909

print(f"Chi-square: {chi2:.4f}")
print(f"P-value: {p_value:.4f}")
print(f"dof: {dof:.4f}")
print(f"expected:\n {expected}") 

# 결과 해석
if p_value < 0.05: 
    print("결과: 성별과 제품 선호도 사이에는 유의미한 관계가 있습니다.") # 성별과 제품 선호도는 서로 관련이 있습니다.
else:
    print("결과: 성별과 제품 선호도는 관계가 없습니다.")

# t-검정이 '평균의 차이'를 비교했다면, 
# 이 검정은 '범주형 데이터(그룹 간의 비율/비중)가 서로 독립적인가?'를 판단합니다.
